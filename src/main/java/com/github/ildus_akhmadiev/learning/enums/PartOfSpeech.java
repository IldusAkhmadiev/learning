package com.github.ildus_akhmadiev.learning.enums;

public enum PartOfSpeech {
    NOUN,          // Существительное
    PRONOUN,       // Местоимение
    VERB,          // Глагол
    ADJECTIVE,     // Прилагательное
    ADVERB,        // Наречие
    PREPOSITION,   // Предлог
    CONJUNCTION,   // Союз
    INTERJECTION,  // Междометие
    ARTICLE,       // Артикль
    DETERMINER,    // Определитель (this, some, my)
    NUMERAL,       // Числительное (one, first, hundred)
    PARTICLE,      // Частица (to в инфинитиве, not)
    MODAL_VERB,    // Модальный глагол (can, must, should)
    EXCLAMATION,   // Восклицание (Hurrah! Bravo!)
    PHRASAL_VERB   // Фразовый глагол (give up, take off)
}
